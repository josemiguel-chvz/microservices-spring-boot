package com.example.reportservice.model;

public class WorkDay {
    private String date;
    private Double hours;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
    public Double getHours() {
        return hours;
    }

    public void setHours(Double hours) {
        this.hours = hours;
    }
}
